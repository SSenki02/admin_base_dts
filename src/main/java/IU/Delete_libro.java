    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id_lib = Integer.parseInt(jTextField7.getText());
         
         Conexion objetoConexion = new Conexion();
        String consulta = "DELETE FROM DJS_LIBRO WHERE ID_LIB=?";
        
        if (jCheckBox1.isSelected()){
            try {
           
             PreparedStatement pt = objetoConexion.establecerConexion().prepareStatement(consulta);
             pt.setInt(1, id_lib);
             pt.execute();
            JOptionPane.showMessageDialog(null, "Eliminacion Exitosa");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar");
            System.out.println(e.toString());
        }
        } else {JOptionPane.showMessageDialog(null,"Por favor marque la casilla");} 
    }//GEN-LAST:event_jButton1ActionPerformed

